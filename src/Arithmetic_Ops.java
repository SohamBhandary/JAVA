public class Arithmetic_Ops {
    public static void main(String[] args) {
        int n=1;
        int m=2;
        int v=m+2;
        v++;
        v--;

        System.out.println(v);
        System.out.println(n+m);
        System.out.println(n-m);
        System.out.println(n*m);
        System.out.println(n/m);


    }
}
